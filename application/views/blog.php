                                <strong>Planned  Activities:</strong>
                                <ul>
                                    <li>Introduce Parma culture within the seven SEP local partner Schools</li>
                                    <li>Expand on indigenous poultry production within the community, targeting the youth – including young women</li>
                                    <li>Initiate mutual and objective collaboration with MIT-USA, to help on the technological approach in improving Agricultural activities, while also providing opportunities and space for Agricultural related research.</li>
                                    <li>Establish value addition/ processing unit, which will enable Agricultural produce processed into finished products, hence improve on the creation of employability and enterprise among the youth.</li>
                                    <li>Develop appropriate and professional Monitoring and Evaluation tool to help in the consistent evaluation of the programme</li>
                                    <li>Locally process own fertilizer/manure- this will reduce the cost of farming, while maximizing on the profits. This will be done through the waste collected from SEP;s existing and functional Ecosan toilet within Oyugis market.</li>
                                    <li>Enhance resource mobilization activities including Funding opportunities to help sustain the programme.</li>
                                </ul>
                                <strong>Possible areas of Collaboration with MIT – D-Lab department: </strong>

                                It is envisaged by SEP leadership that the above cited planned activities are suitable points for collaboration. We therefore reach out to MIT – D-Lab (Class of Agriculture) to consider sharing any inputs in this context.

                                <strong>SEP Football Program </strong>

                                SEP aims to use football to mobilize, engage, and educate youth on life skills, health issues, and a wide variety of other topics. Football is the channel that we use to deliver key messages to the youths in our community. Through the power of “developmental football,” SEP’s specially trained coaches provide not only fun, but important life skills to develop the next generation of community leaders.

                                SEP’s unique Youth Football Program leverages the power of football as a tool to:
                                <ul>
                                    <li>Teach life skills through games and integrated messaging in areas such as HIV/AIDS prevention, health and sanitation, Agriculture &amp; nutrition, reproductive health, peace building, and substance abuse</li>
                                </ul>
                                <ul>
                                    <li>Engage youth in healthy, fun social outlets</li>
                                    <li>Improve gender equality</li>
                                    <li>Mobilize youth and the community at large</li>
                                    <li>Develop youth leadership through training in coaching, refereeing and tournament management etc</li>
                                </ul>
                                <br><img class="alignnone size-medium wp-image-626" src="/assets/uploads/2021/02/picagric0-300x190.jpg" alt="" width="300" height="190" />
                                <br><br>
                                <strong>Impact and Accomplishments:</strong>
                                <ul>
                                    <li>1250 youth  between the ages of 10 and 20  participate in ongoing football practice and matches</li>
                                    <li>65% of all participants are girls and young women who would otherwise not have a social or athletic outlet outside of their traditional roles</li>
                                    <li>100 youth have been trained in football coaching, refereeing, and First Aid and 20 are now volunteering as coaches and referees at local schools</li>
                                    <li>Have a football presence in 7 primary schools and 2 secondary schools.</li>
                                    <li>Hosts 3 football events per year focused on key life skill topics, resulting in massive behavior change among the youth. These are held in the months of April, August and December (During School Holidays).</li>
                                </ul>
