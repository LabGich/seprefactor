                                    <li>Register SEP Girls Soccer in the Kenya Premier League – Girls in this part of the village, Oyugis, have been side lined in talent development, hence this will be the opportunity to make the girls show that they are important part of the community, people with talent who can compete Nationally and achieve results. We want the girls from Oyugis region, an area that has been left out ij sports for a long time, to be seen as reliable role models to other young girls.</li>
                                    <li>Expand on resource mobilization focusing on soccer equipments so as to sustain and expand soccer programmes in the area</li>
