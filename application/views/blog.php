                                <h5>Day 1</h5>
                                On the 8<sup>th</sup> of September, 2021, Social Empowerment Project, hosted Bundesliga Youth Ambassadors (BUYAS) for a self- paced comprehensive learning program. The BUYAS arrived on the evening of 7<sup>th</sup> September, 2021.
