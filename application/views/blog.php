                                <p><strong>Youth Self-help Agriculture Program</strong>

                                    SEP, through its innovative Youth Self- help Agriculture Program, helps ensure food security, financially empowers youth,  (girls and boys) provides valuable agricultural training, and supports youth’s access to education.  SEP works with local families to dedicate a small percent of the families’ own crop land to the care of a youth family member.  The child is then responsible for planting, maintaining, and harvesting this plot.  After basic food security needs of the family are met, all proceeds of crop sales from the child’s plot go to the child and are used to fund a portion of their school-related expenses.  The families must commit to funding the remaining portion of the school expenses.

                                    <strong>SEP’s Role:</strong>
                                <ul>
                                    <li>Coordinates the dedication of the plot to the youth family member</li>
                                    <li>Provides an initial allotment of seed to the youth to establish his/her crop</li>
                                    <li>Provides free fertilizer to the youth on an ongoing basis</li>
                                    <li>Provides the youth with technical training in high-yield farming techniques, including fertilizer application</li>
                                    <li>Provides market price advisory services for crop sales</li>
                                    <li>Provides ongoing monitoring and evaluation of the youth’s plot and his/her school engagement<strong> </strong></li>
                                </ul>
                                <strong>Impact and Accomplishments:</strong>
                                <ul>
                                    <li>125 youth have been able to attend primary and secondary that otherwise could not afford an education, out of this number, 35 have gone through tertiary education through the support from agricultural produce.</li>
                                    <li>Basic food security directly ensured for  the said 125 families</li>
                                    <li>353 youth have been trained in key agricultural-related areas,</li>
                                    <li>Over ksh.70,000 has been generated by youth for the support of their education in a period of 2 years</li>
                                    <li>10 HIV/AIDs-related orphans’ school fees have been 100% subsidized by SEP</li>
                                </ul>
                                <br>
                                <br>
                                <strong>Ongoing Activities:</strong><br>
                                <ul>
                                    <li>SEP has introduced indegeniuos poultry farming, with only 5 youths in the pilot phase</li>
                                    <li>SEP has produced training curriculum for Agriculture and Agribusiness trainings</li>
                                    <li>SEP is currently developing intergrated soccer coaching games, that will enable linking of Agriculture and Soccer in the pitch</li>
                                    <li>Horticultural Programme is ongoing with such crops like maize, beans, ground nuts, bananas, indegenious vegetables and other legumenuos crops being planted. All these are done at the necleaus unit where SEP trains its members.</li>
                                </ul>
                                <br><img class="alignnone size-medium wp-image-624" src="/assets/uploads/2021/02/picagric-300x225.jpg" alt="" width="300" height="225" /><img class="alignnone size-medium wp-image-627" src="/assets/uploads/2021/02/picagric2-300x225.jpg" alt="" width="300" height="225" />
