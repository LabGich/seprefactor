                                <img class="size-medium wp-image-1006" src="/assets/uploads/2021/09/IMG_0070-300x168.jpg" alt="" width="300" height="168" />
                                <br><br>
                                This program a partnership with TSG 1899 Hoffenheim, street footballworld and Transforming Young Stars of Africa was to enable the BUYAS interact and learn from each other and  share what activities they undertake at their respective organizations. The wins and the challenges as well. A few success stories from the BUYAS and some challenging stories encouraged everyone to keep doing what they do and not to give up.

                                A moving and encouraging speech from the director of S.E.P, Mr. Festus Juma, was a highlight of day 1, with rich lessons on mental health, reproductive health and healthy relationships being the main message. He welcomed the BUYAS to Oyugis town, Homa Bay County and briefed them on the activities that the S.E.P- BUYAS have been undertaking. Apart from empowering the community and young girls through sports, they empower them economically to through skill based projects.

                                &nbsp;
                                <ul>
                                    <li>
                                        <h5>Society Empowerment Project empowers communities through sports</h5>
                                    </li>
                                </ul>
                                &nbsp;

                                The afternoon session involved showing the BUYAS what S.E.P does in the communities. This involved a visit to the farms where they do poultry and horticulture farming. The farming is done by SEP members in the community. The farmers are given partitions of land and fertilizers, as well as poultry and poultry feeds, which they use to farm and eventually get food and income. This is part of the economic empowerment.

                                SEP, CO-director, Lourraine, also briefed the BUYAS on the reproductive health department, where she showed them how the department makes and manages re-usable sanitary pads. In her talk, she talked about menstrual hygiene, pads drives and the reproductive health sessions that they hold with the young and adolescent girls. This department also takes credit for empowering the young women and girls through tailoring courses.

